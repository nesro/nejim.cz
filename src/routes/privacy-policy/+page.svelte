<p>
    Naše webové stránky používají soubory cookies. Soubor cookie je soubor obsahující identifikátor
    (řetězec písmen a čísel), který je webovým serverem odeslán webovému prohlížeči a webovým
    prohlížečem uložen. Tento identifikátor je poté odeslán zpět serveru vždy, když si prohlížeč od

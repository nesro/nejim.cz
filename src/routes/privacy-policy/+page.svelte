    vypršení, pokud před datem vypršení nejsou smazány uživatelem. Relační soubory cookies naopak
    vyprší na konci relace uživatele po zavření webového prohlížeče. Soubory cookies běžně
    neobsahují žádné informace, které by uživatele jednoznačně identifikovaly, ale osobní údaje,
    které o vás ukládáme, mohou být s informacemi uloženými a získanými ze souborů cookies
    propojeny.
</p>
